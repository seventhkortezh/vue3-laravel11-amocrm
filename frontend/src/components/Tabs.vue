<template>
  <div>
    <div class="sm:hidden">
      <label for="tabs" class="sr-only">Выберите:</label>
      <select id="tabs" name="tabs" class="block w-full rounded-md border-gray-300 py-2 pl-3 pr-10 text-base focus:border-indigo-500 focus:outline-none focus:ring-indigo-500 sm:text-sm">
        <option v-for="tab in tabs" :key="tab.name" :selected="tab.current">{{ tab.name }}</option>
      </select>
    </div>
    <div class="hidden sm:block">
      <div class="border-b border-gray-200">
        <nav class="-mb-px flex space-x-8" aria-label="Tabs">
          <a v-for="tab in tabs" :key="tab.name" :href="tab.href" :class="[tab.current ? 'border-indigo-500 text-indigo-600' : 'border-transparent text-gray-500 hover:border-gray-300 hover:text-gray-700', 'whitespace-nowrap border-b-2 px-1 py-4 text-sm font-medium']" :aria-current="tab.current ? 'page' : undefined">{{ tab.name }}</a>
        </nav>
      </div>
    </div>
  </div>
</template>

<script setup>
import { ref, onMounted, watch } from 'vue';
import { useRouter } from 'vue-router';

const router = useRouter();
const tabs = ref([
  { name: 'Сделки', href: '/', current: false },
  { name: 'История', href: '/history', current: false },
]);

const setCurrentTab = () => {
  const currentPath = router.currentRoute.value.path;
  tabs.value.forEach(tab => {
    tab.current = (tab.href === currentPath);
  });
};

onMounted(setCurrentTab);

watch(() => router.currentRoute.value.path, () => {
  setCurrentTab();
});
</script>
